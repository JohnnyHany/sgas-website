// Type-safe translation helper
type TranslationValue = { en: string; ar: string } | ((...args: unknown[]) => string);

type DeepStringObj = { [key: string]: string | DeepStringObj };

function t(section: string, key: string, lang: Language): string {
  const keys = key.split(".");
  let obj: any = (translations as any)[section];

  for (const k of keys) {
    if (obj && typeof obj === "object" && k in obj) {
      obj = obj[k];
    } else {
      return key;
    }
  }

  if (typeof obj === "object" && obj !== null && lang in obj) {
    return obj[lang];
  }
  if (typeof obj === "string") return obj;
  return key;
}

